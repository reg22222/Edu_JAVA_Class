import java.util.*;
public class Exam_10 {
	public static void main(String[] args) {
		int su;
		for(int i = 0 ; i<10 ; ++i)
			su = (int)(Math.random() * 10)+1;
			System.out.println("임의의 수 : "+ su);
			//0.0에서 0.9999... 사이의 임의의 수가 나온다.
			//1~10까지 수중 랜덤한 값 구하기 : *10을 해주고 이 값을 int로 형변환 그리고 1을 더해줌
			//0~10까지 수 중 랜덤한 값 구하기 : *11을 해주고 int로 형변호나
		
	}

}
